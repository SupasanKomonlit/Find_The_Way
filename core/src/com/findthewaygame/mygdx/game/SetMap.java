	public int[][] GetMap(String MapText){
		int Count = 0;
		for(int y = AllY-1; y > (-1); y -= 1) {
			for(int x = 0; x < AllX; x += 1, Count += 1) {
				System.out.println("y : "+y+" x : "+x+" Count : "+Count);
				this.MapArray[x][y] = Integer.parseInt(Character.toString((MapText.charAt(Count))));
			}
		}
		PrintMap();
		return this.MapArray;
	}
	
